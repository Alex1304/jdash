package jdash.common;

/**
 * Represents a leaderboard type.
 */
public enum LeaderboardType {

    /**
     * Top players with the most stars.
     */
    TOP,

    /**
     * Friends leaderboard.
     */
    FRIENDS,

    /**
     * Leaderboard relative to the current player.
     */
    RELATIVE,

    /**
     * Top players with the most creator points.
     */
    CREATORS
}